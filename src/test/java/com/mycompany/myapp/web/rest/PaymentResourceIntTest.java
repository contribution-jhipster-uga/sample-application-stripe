    @Test
    @Transactional
    public void updatePayment() throws Exception {
        // Initialize the database
        paymentRepository.saveAndFlush(payment);

        int databaseSizeBeforeUpdate = paymentRepository.findAll().size();

        // Update the payment
        Payment updatedPayment = paymentRepository.findById(payment.getId()).get();
        // Disconnect from session so that the updates on updatedPayment are not directly saved in db
        em.detach(updatedPayment);
        updatedPayment
            .date(UPDATED_DATE)
            .token(UPDATED_TOKEN)
            .currency(UPDATED_CURRENCY)
            .amount(UPDATED_AMOUNT)
            .description(UPDATED_DESCRIPTION)
            .capture(UPDATED_CAPTURE)
            .receipt(UPDATED_RECEIPT);

        restPaymentMockMvc.perform(put("/api/payments")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedPayment)))
            .andExpect(status().isOk());

        // Validate the Payment in the database
        List<Payment> paymentList = paymentRepository.findAll();
        assertThat(paymentList).hasSize(databaseSizeBeforeUpdate);
        Payment testPayment = paymentList.get(paymentList.size() - 1);
        assertThat(testPayment.getDate()).isEqualTo(UPDATED_DATE);
        assertThat(testPayment.getToken()).isEqualTo(UPDATED_TOKEN);
        assertThat(testPayment.getCurrency()).isEqualTo(UPDATED_CURRENCY);
        assertThat(testPayment.getAmount()).isEqualTo(UPDATED_AMOUNT);
        assertThat(testPayment.getDescription()).isEqualTo(UPDATED_DESCRIPTION);
        assertThat(testPayment.isCapture()).isEqualTo(UPDATED_CAPTURE);
        assertThat(testPayment.getReceipt()).isEqualTo(UPDATED_RECEIPT);
    }

